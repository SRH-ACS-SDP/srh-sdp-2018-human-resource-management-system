package sdpsose2018.hrms;



/**
 * @author Deepthi
 *
 */
public class PayRollReport {
	
	private int employeeid;
	private String employeename;
	private String gross_salary;
	private String net_salary;
	private Double tax;

	/**
	 * @return the employeeid
	 */
	public int getEmployeeid() {
		return employeeid;
	}
	/**
	 * @param employeeid the employeeid to set
	 */
	public void setEmployeeid(int employeeid) {
		this.employeeid = employeeid;
	}
	/**
	 * @return the employeename
	 */
	public String getEmployeename() {
		return employeename;
	}
	/**
	 * @param employeename the employeename to set
	 */
	public void setEmployeename(String employeename) {
		this.employeename = employeename;
	}
	
	/**
	 * @return the gross_salary
	 */
	public String getGross_salary() {
		return gross_salary;
	}
	/**
	 * @param gross_salary the gross_salary to set
	 */
	public void setGross_salary(String gross_salary) {
		this.gross_salary = gross_salary;
	}
	/**
	 * @return the net_salary
	 */
	public String getNet_salary() {
		return net_salary;
	}
	/**
	 * @param net_salary the net_salary to set
	 */
	public void setNet_salary(String net_salary) {
		this.net_salary = net_salary;
	}
	/**
	 * @return the tax
	 */
	public Double getTax() {
		return tax;
	}
	/**
	 * @param result the tax to set
	 */
	public void setTax(Double result) {
		this.tax = result;
	}
	
	
}
	
	
	

